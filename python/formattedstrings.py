################### Formatted Strings #################
first = "Yahshua Emmanuel"
last = "Ball"
full = first + " " + last
print(full)

first = "Yahshua Emmanuel"
last = "Ball"
full = f"{first} {last}"
print(full)

first = "Yahshua Emmanuel"
last = "Ball"
full = f"{len(first)} {last}"
print(full)

first = "Yahshua Emmanuel"
last = "Ball"
full = f"{len(first)} {8 + 8}"
